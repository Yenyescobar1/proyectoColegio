    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        controladorEstudiante ctr = new controladorEstudiante();

        if (txtIdentificacion.getText().isEmpty()
                || txtNombre.getText().isEmpty()
                || txtApellido.getText().isEmpty()
                || txtEdad.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Completar todos lo campos");
            return;
        }
        ctr.actualizarEstudiante(txtIdentificacion.getText(), txtNombre.getText(), txtApellido.getText(), Integer.parseInt(txtEdad.getText()));
        JOptionPane.showMessageDialog(null, "Actualizado");
        limpiar();
        mostrarTablaEstudiantes();

    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (!txtIdentificacion.getText().isEmpty()) {
            String identificacion = txtIdentificacion.getText();
            boolean eliminado = false;
            for (estudiante p : ListadoEstudiantes) {
                if (p.getIdentificación().equals(identificacion)) {
                    ListadoEstudiantes.remove(p);
                    eliminado = true;
                    JOptionPane.showMessageDialog(null, "Estudiante eliminado");
                    limpiar();
                    break;
                }
            }
            if (!eliminado) {
                JOptionPane.showMessageDialog(null, "No existe persona con esa identificacion");
            } else {
                mostrarTablaEstudiantes();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Ingresar identificacion para eliminar");
        }
        mostrarTablaEstudiantes();

        // TODO add your handling code here:
    }//GEN-LAST:event_btnEliminarActionPerformed

