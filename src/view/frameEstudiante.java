    controladorEstudiante control = new controladorEstudiante();

    private void mostrarTablaEstudiantes() {
        DefaultTableModel tabla = (DefaultTableModel) tblEstudiantes.getModel();
        tabla.setRowCount(0);
        List<estudiante> personas = control.consultarListaEstudiantes();

        for (int i = 0; i < personas.size(); i++) {
            estudiante Persona = personas.get(i);
            tabla.addRow(new String[]{Persona.getIdentificación(),
                Persona.getNombre(),
                Persona.getApellido(),
                String.valueOf(Persona.getEdad())});

        }
    }

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        controladorEstudiante ctr = new controladorEstudiante();

        if (txtIdentificacion.getText().isEmpty()
                || txtNombre.getText().isEmpty()
                || txtApellido.getText().isEmpty()
                || txtEdad.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Completar todos lo campos");
            return;
        }

        if (ctr.verificarSiExisteEstudiante(txtIdentificacion.getText())) {
            JOptionPane.showMessageDialog(null, "El alumno ya esta registrado");
            txtIdentificacion.setText("");
            return;
        }
        if (!isNumeric(txtIdentificacion.getText())) {
            JOptionPane.showMessageDialog(null, "Digite solo numeros en la identificacion");
            return;
        }
        if (!isNumerico(txtEdad.getText())) {
            JOptionPane.showMessageDialog(null, "Digite solo numeros en la edad");

            return;
        }
        ctr.resgistrarEstudiantes(txtIdentificacion.getText(), txtNombre.getText(), txtApellido.getText(), Integer.parseInt(txtEdad.getText()));
        JOptionPane.showMessageDialog(null, "Estudiante registrado");
        limpiar();
        mostrarTablaEstudiantes();

        // TODO add your handling code here:
    }//GEN-LAST:event_btnAgregarActionPerformed

