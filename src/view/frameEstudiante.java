    public boolean isNumeric(String identificacion) {
        try {
            Long.parseLong(identificacion);
            return true;
        } catch (NumberFormatException e) {
            return false;
        }
    }

    public void limpiar() {
        txtIdentificacion.setText("");
        txtNombre.setText("");
        txtApellido.setText("");
        txtEdad.setText("");
    }

    public boolean isNumerico(String edad) {
        try {
            Integer.parseInt(edad);

            return true;
        } catch (NumberFormatException e) {
            return false;
        }
    }

