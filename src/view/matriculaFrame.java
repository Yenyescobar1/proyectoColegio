   public void mostrarInformacion() {
        int numeroEstudiantes = controladorEstudiante.consultarListaEstudiantes().size();
        DefaultComboBoxModel modelo = (DefaultComboBoxModel) listaAlumnos.getModel();
        listaAlumnos.setModel(modelo);
        for (int i = 0; i < numeroEstudiantes; i++) {
            modelo.addElement(
                    controladorEstudiante.consultarListaEstudiantes().get(i).getNombre()
                    +" "+
                    controladorEstudiante.consultarListaEstudiantes().get(i).getApellido()
            );
        }
        int numeroCursos = controladorCurso.consultarListaCurso().size();
        DefaultComboBoxModel model = (DefaultComboBoxModel) listaCursos.getModel();
        listaCursos.setModel(model);
        for (int i = 0; i < numeroCursos; i++) {
            model.addElement(controladorCurso.consultarListaCurso().get(i));

        }
    }
    private void btnMatricularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMatricularActionPerformed
        curso Curso = (curso) listaCursos.getSelectedItem();
        estudiante Estudiante = (estudiante) listaAlumnos.getSelectedItem();
        boolean matricula = estudianteConsulta.matricularCurso(Curso, Estudiante);

        if (matricula) {
            JOptionPane.showMessageDialog(null, "Estudiante matriculado");

        } else {
            JOptionPane.showMessageDialog(null, "No se puede realizar el proceso de matricula");
        }
        mostrarMatriculas();

        // TODO add your handling code here:
    }//GEN-LAST:event_btnMatricularActionPerformed

