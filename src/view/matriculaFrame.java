    private void mostrarMatriculas() {
        controladorEstudiante ctr = new controladorEstudiante();
        DefaultTableModel tabla = (DefaultTableModel) tblMatriculas.getModel();
        tabla.setRowCount(0);
        List<estudiante> personas = ctr.consultarListaEstudiantes();

        for (int i = 0; i < personas.size(); i++) {
            estudiante Persona = personas.get(i);
            tabla.addRow(new String[]{
                Persona.getNombre(),
                String.valueOf(Persona.getCursosMatriculados())
            });

        }
    }

